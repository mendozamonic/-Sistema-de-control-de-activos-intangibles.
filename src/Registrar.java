    private String genIdIntangible() {
        return "LIC" + String.format("%06d", (int) (System.currentTimeMillis() % 1_000_000));
    }

    private String genCodigo() {
        return "COD-" + System.currentTimeMillis();
    }

    private LocalDate parseFecha(String s) {
        if (s == null) {
            return null;
        }
        s = s.trim();
        if (s.isEmpty()) {
            return null;
        }

        String[] patrones = {"d/M/yyyy", "dd/MM/yyyy", "yyyy-MM-dd"};
        for (String p : patrones) {
            try {
                return LocalDate.parse(s, java.time.format.DateTimeFormatter.ofPattern(p));
            } catch (Exception ignore) {
            }
        }
        return null;
    }

    private String aVidaTexto(String nStr) {
        if (nStr == null || nStr.trim().isEmpty()) {
            return "12 meses";
        }
        String d = nStr.replaceAll("\\D+", "");
        if (d.isEmpty()) {
            return "12 meses";
        }
        int n = Integer.parseInt(d);
        return (n == 1) ? "1 mes" : (n + " meses");
    }

