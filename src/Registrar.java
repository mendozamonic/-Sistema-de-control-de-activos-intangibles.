    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombreIntangible = txtNombre1.getText().trim();
        String version = txtVersion.getText().trim();
        String nombreProv = txtNombreProveedor.getText().trim();
        String tipoLic = cmbTipo.getSelectedItem().toString();
        String vidaTxt = aVidaTexto(txtVidaUtil.getText());
        LocalDate fAdq = parseFecha(txtFechaAdq.getText());
        LocalDate fVenc = parseFecha(txtFechaVenc.getText());

        if (nombreIntangible.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El Nombre es obligatorio.", "Validación", JOptionPane.WARNING_MESSAGE);
            txtNombre1.requestFocus();
            return;
        }
        if (version.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La Versión es obligatoria.", "Validación", JOptionPane.WARNING_MESSAGE);
            txtVersion.requestFocus();
            return;
        }
        if (nombreProv.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El Nombre del Proveedor es obligatorio.", "Validación", JOptionPane.WARNING_MESSAGE);
            txtNombreProveedor.requestFocus();
            return;
        }

        java.math.BigDecimal costo;
        try {
            costo = new java.math.BigDecimal(txtCosto.getText().trim());
            if (costo.compareTo(java.math.BigDecimal.ZERO) < 0) {
                throw new NumberFormatException();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Costo inválido. Usa números (ej: 200 o 200.00).", "Validación", JOptionPane.WARNING_MESSAGE);
            txtCosto.requestFocus();
            return;
        }

        if (fAdq == null) {
            fAdq = LocalDate.now();
        }
        if (fVenc == null) {
            String d = vidaTxt.replaceAll("\\D+", "");
            if (!d.isEmpty()) {
                fVenc = fAdq.plusMonths(Integer.parseInt(d));
            }
        }

        // ======= ACTUALIZAR =======
        if (esEdicion) {
            String sql = "UPDATE intangible "
                    + "SET nombre_intangible=?, version=?, nombre_proveedor=?, "
                    + "    tipo_licencia_=?, fecha_adquisicion=?, fecha_vencimiento=?, "
                    + "    costo=?, vida_util=? "
                    + "WHERE codigo_=?";

            try (java.sql.Connection cn = Conexion.getConnection(); java.sql.PreparedStatement ps = cn.prepareStatement(sql)) {

                ps.setString(1, nombreIntangible);
                ps.setString(2, version);
                ps.setString(3, nombreProv);
                ps.setString(4, tipoLic);
                ps.setDate(5, java.sql.Date.valueOf(fAdq));
                if (fVenc != null) {
                    ps.setDate(6, java.sql.Date.valueOf(fVenc));
                } else {
                    ps.setNull(6, java.sql.Types.DATE);
                }
                ps.setBigDecimal(7, costo);
                ps.setString(8, vidaTxt);
                ps.setString(9, codigoOriginal);

                int n = ps.executeUpdate();
                JOptionPane.showMessageDialog(this, (n > 0 ? "Actualizado " : "Sin cambios."));
                dispose();
                return;

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al actualizar: " + ex.getMessage(), "BD",
                        JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
                return;
            }
        }

        // ======= INSERT =======  
        String idIntangible = genIdIntangible();
        String codigo = genCodigo();
        String estado = "ACTIVO";

        String sql = "INSERT INTO intangible "
                + "(idintangible, version, nombre_proveedor, tipo_licencia_, codigo_, costo, vida_util, estado_, nombre_intangible, fecha_adquisicion, fecha_vencimiento) "
                + "VALUES (?,?,?,?,?,?,?,?,?,?,?)";

        try (java.sql.Connection cn = Conexion.getConnection(); java.sql.PreparedStatement ps = cn.prepareStatement(sql)) {

            ps.setString(1, idIntangible);
            ps.setString(2, version);
            ps.setString(3, nombreProv);
            ps.setString(4, tipoLic);
            ps.setString(5, codigo);
            ps.setBigDecimal(6, costo);
            ps.setString(7, vidaTxt);
            ps.setString(8, estado);
            ps.setString(9, nombreIntangible);
            ps.setDate(10, java.sql.Date.valueOf(fAdq));
            if (fVenc != null) {
                ps.setDate(11, java.sql.Date.valueOf(fVenc));
            } else {
                ps.setNull(11, java.sql.Types.DATE);
            }

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Guardado \nID: " + idIntangible + "\nCódigo: " + codigo);

            // limpiar
            txtNombre1.setText("");
            txtVersion.setText("");
            txtNombreProveedor.setText("");
            txtIDProveedor.setText("");
            txtCosto.setText("");
            txtVidaUtil.setText("");
            txtFechaAdq.setText("");
            txtFechaVenc.setText("");
            cmbTipo.setSelectedIndex(0);
            txtNombre1.requestFocus();

        } catch (java.sql.SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al guardar: " + ex.getMessage(), "Base de datos", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

